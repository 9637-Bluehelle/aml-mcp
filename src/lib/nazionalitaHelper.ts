const _nazToNazione = new Map<string, string>();
const _isoToNaz = new Map<string, string>();
const _nazioneToNaz = new Map<string, string>();
for (const n of NAZIONALITA) {
  _nazToNazione.set(n.nazionalita.toLowerCase(), n.nazione);
  if (n.iso) _isoToNaz.set(n.iso, n.nazionalita);
  _nazioneToNaz.set(n.nazione.toLowerCase(), n.nazionalita);
}

/** Data una nazionalità (es. "Tedesca"), restituisce la nazione (es. "Germania") */
export function getNazioneByNazionalita(nazionalita: string): string | null {
  if (!nazionalita) return null;
  return _nazToNazione.get(nazionalita.toLowerCase()) || null;
}

/** Dato un codice ISO (es. "IT", "DE"), restituisce la nazionalità (es. "Italiana", "Tedesca") */
export function getNazionalitaByISO(isoCode: string): string | null {
  if (!isoCode) return null;
  return _isoToNaz.get(isoCode.toUpperCase()) || null;
}

/** Normalizza un valore di nazionalità: accetta aggettivo ("Italiana"), nazione ("Italia"), ISO ("IT") */
export function normalizeNazionalita(value: string | undefined | null): string {
  if (!value) return 'Italiana';
  const trimmed = value.trim();
  if (!trimmed) return 'Italiana';
  // Match per aggettivo (es. "Italiana", "Tedesca")
  const found = NAZIONALITA.find(n => n.nazionalita.toLowerCase() === trimmed.toLowerCase());
  if (found) return found.nazionalita;
  // Match per codice ISO (es. "IT", "DE")
  const byIso = _isoToNaz.get(trimmed.toUpperCase());
  if (byIso) return byIso;
  // Match per nome nazione (es. "Italia", "Germania")
  const byNazione = _nazioneToNaz.get(trimmed.toLowerCase());
  if (byNazione) return byNazione;
  // Fallback: restituisci il valore originale
  return trimmed;
}

/** Verifica se la nazionalità è italiana */
export function isItaliana(nazionalita: string | undefined | null): boolean {
  if (!nazionalita) return true; // default italiana
  return nazionalita.toLowerCase() === 'italiana';
}
